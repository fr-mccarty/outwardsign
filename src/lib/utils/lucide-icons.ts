/**
 * Get a Lucide icon component by name
 *
 * @param iconName - The name of the Lucide icon (e.g., 'Heart', 'Cross', 'Droplet')
 * @returns The corresponding Lucide icon component, or FileText as default if not found
 *
 * @example
 * const Icon = getLucideIcon('Heart')
 * return <Icon className="h-4 w-4" />
 */
export function getLucideIcon(iconName: string): LucideIcon {
  return LUCIDE_ICON_MAP[iconName] || FileText
}
